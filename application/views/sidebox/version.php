            <td class="version">0.4.3</td>

            <td class="version">25.05.2011</td>

            <td class="desc"><ul>
<li>Característica – Cambio de la membresía en alianzas:
<ul>
<li>Ya no se abandona la alianza de inmediato, sino que se permanece 24 horas en ella, aunque sin derechos. Lo mismo se aplica cuando un jugador es expulsado de una alianza. Este plazo sirve para evitar que los jugadores puedan provocar la dispersión inmediata de las tropas apostadas en su ciudad abandonando su alianza.</li>
<li>Las alianzas no se disuelven de inmediato, sino que también tienen una demora de 24 horas.</li>
<li>Tras haber dejado efectivamente una alianza, es preciso esperar 24 horas para acceder de nuevo a otra (o la misma).</li>
</ul></li>

<li>Característica – Rebelión revisada:
<ul>
<li>Las “Rebeliones” se desarrollan DENTRO de las ciudades y sin muros, independientemente de la cantidad de unidades que luchen. El nivel de la ciudad determinará el tamaño del campo de batalla.</li>
<li>“Liberar ciudad” se realiza con un muro (según el límite de guarnición).</li>
<li>Si un dueño intenta liberar su ciudad accediendo a una batalla mediante la función “Rebelión”, la batalla pasa a ser interna (sin murallas).</ li>
<li>Ahora está disponible la “Rebelión” también sin unidades, en caso de que el ocupante no tenga unidades apostadas en la ciudad o el puerto.
<li>DURANTE una rebelión, la ciudad sigue estando ocupada. El dueño de la ciudad no recupera el control sobre esta hasta que es liberada de forma efectiva. </li>
<li>Las ciudades y los puertos se liberan cuando unidades no confederadas del ocupante invaden con éxito la ciudad o el puerto. Así, por ejemplo, una ciudad ocupada, que es saqueada por otro, queda liberada.</li>
</ul></li>

<li>Característica – El puerto de una ciudad pasa a ser un “lugar” en sí mismo:
<ul>
<li>Ya no hay más “Bloquear puerto” durante 1-8 horas, sino que ahora se pueden ocupar los puertos de forma permanente. Esta ocupación sólo podrá cancelarse mediante la liberación o la renuncia voluntaria. La misión funciona ahora de forma análoga a la de Ocupar ciudad: los barcos pueden permanecer apostados en el puerto de forma ilimitada y llear acabo misiones desde ahí. </li>
<li>ATENCIÓN: Hasta ahora, al ocupar una ciudad se controlaba también el puerto, mientras que ahora son dos lugares independientes. Por lo tanto, lo mejor es ocupar los dos, ya que la presencia de barcos enemigos en el puerto (también barcos de guerra del ocupante) cortan el paso al dueño de la ciudad.</li>
<li>Ahora sólo hay un dueño definido del puerto, para que en el futuro esté claro quién controla el acceso al mismo. Ya no es posible bloquear “conjuntamente”. Si varias partes no confederadas llegan a un puerto al mismo tiempo para ocuparlo, se iniciará automáticamente una batalla después de la primera batalla contra el defensor, hasta que quede un vencedor.</li>
<li>Mientras se ocupe únicamente el puerto y no la ciudad, se seguirá siendo enemigo respecto de ésta y no podrá pasar ninguna misión marítima por este bloqueo. Si un jugador ocupa al mismo tiempo la ciudad y el puerto, las misiones pacíficas del dueño podrán pasar por el puerto ocupado. </li>
<li>Sin la investigación  “Gobernador” puede ocuparse un único puerto. Para todas las demás ocupaciones simultáneas de ciudades o puertos es necesaria esa investigación. Así, sólo podrá ocuparse una cantidad de puertos equivalente al nivel de palacio +1. El límite para ocupación de ciudades permanece sin cambios.</li>
</ul></li>

<li>Característica – Diferentes vistas revisadas:
<ul>
<li>Representación correcta de tropas y barcos aliados/apostados en el consejero Premium.</li>
<li>Las ocupaciones de puertos y ciudades se muestran también en el consejero militar y pueden cancelarse también ahí (siempre y cuando no haya ninguna unidad más en la ciudad o el puerto).</li>
<li>Ahora los barcos mercantes perdidos se muestran en el consejero militar como tales y ya no se cuentan como barcos de guerra perdidos.</li>
<li>Se han revisado las marcas de tiempo durante las batallas, de modo que cada una tiene la suya propia, que muestra el tiempo correcto que falta para la siguiente ronda.</li>
<li>En el informe de espionaje de las guarniciones se ven ahora todas las listas correctamente.</li>
<li>Colores de las islas en el mapa (por orden de importancia):<br>
Una ciudad propia u ocupada por el jugador se muestra en la isla -> azul<br>
Una ciudad aliada o una ciudad ocupada por un aliado se muestra en la isla -> verde<br>
Una ciudad con derecho de guarnición se muestra en la isla -> amarilla</li>
<li>Cambios en los mensajes del consejero de la ciudad: si se ocupa una ciudad de una isla en la que se ubica una ciudad propia, ésta se menciona en el mensaje. Si en la isla hay varias ciudades propias, se menciona aquella que tiene el ID menor.</li>
<li>Ahora se enlazan todas las ciudades en las que se tenga derecho de guarnición en las zonas de “acuerdos”.</li>
</ul></li>

<li>Característica – Se ha ampliado la función de retirada de unidades:
<ul>
<li>Las unidades sólo pueden retirarse en oleadas completas. Necesitan para ello una hora más el tiempo de viaje a su último punto de partida.</li>
<li>Si se dispersan todas las oleadas, regresan a su ciudad de origen.</li>
</ul></li>

<li>Característica – Cambios en el sistema de batalla:
<ul>
<li>Si hay una batalla en la propia ciudad, las unidades en retirada participan automáticamente cuando hay unidades propias implicadas.</li>
<li>Las unidades que terminen de formarse durante una batalla participan automáticamente en la ronda siguiente si hay unidades propias implicadas.</li>
<li>Ahora también está disponible la retirada parcial para barcos si están luchando en la ciudad propia.</li>
<li>Los informes de batalla contienen el nombre de la ciudad de la que partieron las unidades, que no necesariamente coincide con su ciudad de origen.</li>
<li>Si tras una batalla quedara más de una parte (p. ej. dos jugadores que quieren ocupar la misma ciudad pero son de alianzas diferentes) se iniciarán batallas de continuación de forma automática hasta que queda un vencedor.</li>
</ul></li>

<li>Característica – Cambios en el comercio y transporte de bienes:
<ul>
<li>Sólo se pagan los bienes que se cargan. Si una carga se interrumpe por la ocupación o el saqueo del puerto, los barcos transportarán sólo una parte de la mercancía. Los bienes no se pagan hasta el FINAL del proceso de carga, cuando se puede comprobar con precisión cuándo se compró realmente.</li>
<li>Las compras y ventas en la tienda se realizan siempre a través del puerto, por lo que los puertos ocupados en la misma isla también serán relevantes.</li>
<li>Los transportes únicamente de mercancías en la misma isla no tienen en cuenta los puertos ocupados.</li>
</ul></li>

<li>Característica – El pueblo de los bárbaros reacciona ahora igual que una ciudad normal (aunque no tiene puerto propio), es decir, se lo puede atacar desde cualquier parte.</li>

<li>Característica – Cambios del Propulsor de Tritón:
<ul>
<li>Ahora es posible realizar una activación durante el transporte, aunque en ese caso sólo se acelera el tiempo de viaje restante. </li>
<li>Está disponible ahora para las colonizaciones.</li>
<li>Está disponible en la tienda.</li>
</ul></li>

<li>Característica – Si se completa un trabajo de creación en el cuartel o el astillero, aparecerá un mensaje en el consejero de la ciudad.</li>

<li>Característica – Ahora, un indicador sobre el tiempo restante de la función premium muestra el tiempo exacto que le queda a cada característica.</li>

<li>Característica – Al cargar unidades, ahora se muestra una vista previa del campo de batalla.</li>

<li>Característica – Cambios generales en la ocupación:
<ul>
<li>Las ciudades o los puertos ocupados ahora también pueden usarse cuando el ocupante activa el modo de vacaciones. La base puede seguir utilizándose, aunque también liberarse. Sin embargo, tras la liberación la ciudad no puede emprender una misión enemiga hasta que el ocupante haya vuelto de las vacaciones.</li>
<li>Si el ocupante de una ciudad o un puerto pasa a modo inactivo, la ocupación también se prolonga hasta la liberación. La misión “Ocupar puerto” provoca en un jugador inactivo la liberación del puerto, tiempo de ocupación: 0 segundos
.</li>
</ul></li>

<li>Característica – El jugador redibe un mensaje en el consejero de la ciudad cuando el general ordena la retirada de las unidades.</li>

<li>Característica – La recompensa del barco mercante en el tutorial ya no está influida por los barcos mercantes que se activaron con invitaciones de amigos.</li>

<li>Corrección – en las alianzas:
<ul>
<li>Se otorgan los rangos automáticos según los puntos.</li>
<li>Se han reelaborado por completo los acuerdos de guarnición para alianzas y jugadores. Ya no se pueden dispersar las unidades por error.</li>
<li>Ahora se trata correctamente el acceso a las alianzas (ocupante/dueño corregido).</li>
<li>Ya no es posible luchar por equivocación contra uno mismo o un miembro de la alianza.</li>
</ul></li>

<li>Corrección – Ahora todos los jugadores pueden ver el tiempo que falta para que vuelvan los barcos mercantes dispersados en el resumen de flotas.</li>

<li>Corrección – Las misiones de saqueo que se envían desde ciudades que no son del jugador (ciudades de la alianza, ocupadas o de guarnición) completan su misión y, al llegar a la base, se trasbordan, es decir, se descarga el 10% para el dueño y para las unidades. Sólo los barcos necesarios para el transporte del resto de bienes a la ciudad de origen siguen el camino para llevarlo allí. Los demás barcos están disponibles para una nueva misión
.</li>

<li>Corrección – En los bloqueos ya no se pierden bienes. Si se dispersan los barcos, sus cargas pasan a la ciudad ocupada.</li>

<li> Corrección – Se han corregido los errores en las condiciones para las investigaciones.</li>

<li> Corrección – Ahora también se puede escribir a jugadores a través de la página de la alianza, es decir, un jugador puede enviar un mensaje privado a otro cuando no tiene derechos para enviar mensajes conjuntos a toda la alianza.</li>

<li> Corrección – Al entregar bienes a ciudades en las que se acabe de construir un edificio de almacenamiento, ahora se tiene en cuenta el límite de almacenamiento correcto.</li>

<li> Corrección – Las opciones de juego guardadas se conservan ahora, por ejemplo, al cambiar la contraseña.</li>

<li> Corrección – Ahora se muestra el mensaje de error correcto al intentar fundar la 13ª ciudad.</li>

<li> Corrección – Se ha reparado un error que impedía enviar un mensaje de advertencia 24 horas antes del final de la característica premium.</li>

<li> Corrección – En el informe de batalla detallado ya no aparecen duplicados los mensajes de maravillas.</li>
