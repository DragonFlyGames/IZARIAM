            <td class="version">14.12.2011</td>

            <td class="desc"><ul>
<li>Característica – Contingente de contrabando
<ul><li>Aquellos jugadores que aún no han investigado la Burocracia pueden transportar 8000 bienes diarios a o de una de sus ciudades ocupadas. Esta cantidad se calcula por ciudad ocupada y no por cuenta.</li>
<li>Si una ciudad está ocupada, no es posible transportar bienes fuera de la ciudad por tierra sin contrabando. El transporte marítimo sigue estando permitido.</li>
<li>Si un puerto está ocupado, no es posible transportar bienes fuera de la ciudad por mar sin contrabando. El transporte por tierra sigue estando permitido.</li>
<li>Si tanto la ciudad como el puerto están controlados, no es posible ningún tipo de transporte de bienes sin contrabando.</li>
</ul>
</li>
<li>Característica – Cambio en el combate marítimo
<ul><li>A partir de ahora, en el combate marítimo hay unidades similares a las del combate en tierra. Los tipos de barco adicionales pueden dividirse en combate cuerpo a cuerpo, a distancia, flanco, primer golpe, carguero y anticarguero. Con este cambio, podréis acceder a tácticas similares a las del combate por tierra.</li> 
<li>Para ello, hay cuatro nuevos tipos de barco disponibles: el Barco lanzamisiles, la Lancha de palas, la Base flotante y el Barco de mantenimiento. Para conseguirlos, en ocasiones es preciso investigar nuevas tecnologías.</li>
<li>Los valores de los tipos de barco ya existentes se han cambiado en algunos casos. También se han adaptado los costes de construcción y mantenimiento.</li>
</ul>
</li>
<li>Característica – Ampliación del foro de alianzas
<ul>
<lI>Los temas contienen ahora un campo, “cambiados recientemente”, en el que se pueden ver el día y la hora del último mensaje. Además se destacan los temas y los hilos no leídos.</li>
<li>Al escribir un mensaje nuevo en el foro, se muestra la cantidad de caracteres disponible.</li>
</ul>
<li>Característica – Cambio en el traslado de ciudad
<ul><li>Si no se puede realizar un traslado, se indica la razón en una ventana de información.</li></ul>
</li>
<li>Característica – El consejero de la ciudad informa sobre la llegada de la bonificación diaria</li>
</ul></td>

